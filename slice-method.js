// slice method

// String ka "slice" method use hota hai JavaScript mein jisse hum kisi bhi string ka ek part nikal sakte hain. Is method ko use karke hum string ke specified index positions ke beech ka portion extract kar sakte hain.

const str = "Hello, how are you";
const sliceString = str.slice(7,10)
console.log(sliceString);  // Output : "how"