// toString method

// JavaScript mein, "toString" method object ko string mein convert karne ke liye use hota hai. Yeh method kisi bhi JavaScript object ko string mein convert kar deta hai.

// Jab bhi hum kisi object ko string mein convert karna chahte hain, hum toString method ka istemal karte hain. Yeh method inherit hota hai JavaScript ke sabhi objects se, kyunki yeh Object prototype mein define hota hai.

const num = 50;
const str = num.toString();
console.log(str); // Output : "50"
console.log(typeof str); // Output : string

